width = int(input("Введите ширину рамки: "))
height = int(input("Введите высоту рамки: "))

# Проходим по каждой строке рамки
for i in range(height):
    # Проходим по каждому символу строки
    for j in range(width):
        # Определяем, какой символ выводить
        if i == 0 or i == height - 1:  # Первая и последняя строки (горизонтальная линия)
            print(" - ", end=" ")
        elif j == 0 or j == width - 1:  # Первая и последняя позиции в строке (вертикальная линия)
            print(" | ", end=" ")
        else:  # Внутренние позиции (пробел)
            print("   ", end=" ")
    print()